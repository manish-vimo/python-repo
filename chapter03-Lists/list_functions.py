lis1 = [1,2,3,4,5,6,7]
print(len(lis1))
lis1.append(4)
print(lis1)
print(lis1.count(4)) # How many 4 are on the list 'lis'?
print(lis1.index(2)) # What is the index of the number 2 in the list 'lis'?
lis1.insert(8, 9) # Add number 9 to the index 8.
print(lis1)
print(max(lis1)) # What is the maximum number in the list?
print(min(lis1)) # What is the minimum number in the list?
print(sum(lis1))